
package funciones;

public class funcionMultiplo {
    public String retornaMultiplo(int num){
          if(num == 0 && num == 0)
          {
           return("TRIFIV");
          }
          else if (num == 0)
          {
          return("TRI");
          }
          else if (num == 0)
          {
          return("FIV");
          }
          return ("Ninguno");
}
}
